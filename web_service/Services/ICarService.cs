/*
 * Интерфейс сервиса для управления автомобилями.
 * Определяет основные операции: добавление автомобиля и получение списка по владельцу.
 * Реализуется в бизнес-логике приложения для взаимодействия с данными.
 */
using web_service.Domain.Entities;

public interface ICarService
{
    // Добавляет автомобиль в систему. 
    // clientProfileId - идентификатор профиля владельца (например: "user_12345")
    // Может выбросить исключение при некорректных данных автомобиля
    Task AddCarAsync(Car car, string clientProfileId);

    // Возвращает все автомобили, принадлежащие указанному владельцу
    // clientProfileId - идентификатор профиля для поиска (например: "user_67890")
    // Если автомобилей нет, возвращает пустую коллекцию
    Task<IEnumerable<Car>> GetCarsByOwnerAsync(string clientProfileId);
}